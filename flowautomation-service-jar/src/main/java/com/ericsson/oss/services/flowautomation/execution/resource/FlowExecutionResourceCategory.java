package com.ericsson.oss.services.flowautomation.execution.resource;

/**
 * The enum Flow execution resource category.
 */
public enum FlowExecutionResourceCategory {

    SETUP("setup"),

    EXECUTE("execute"),

    REPORT("report");

    private final String name;

    FlowExecutionResourceCategory(final String name) {
        this.name = name;
    }

    /**
     * Gets name.
     *
     * @return the name
     */
    public String getName() {
        return name;
    }
}
